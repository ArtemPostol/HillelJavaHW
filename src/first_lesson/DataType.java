package first_lesson;

public class DataType {
    public static void main(String[] args) {
        // приметивные типы данных в Java

        //целочисленные
        byte b = -127;
        short s = -32768;
        int x = 5; // самый популярный
        long l = 9L;

        // с плавающей точкой
        float Float = 830303303030.20F;
        double d = 12312321.02; // популярный

        // символьные
        char c = 'A';

        //логические
        boolean bl = true;

        //ссылочные
        String itsAWord = "It's a string"; // популярный

        int firstCatAge; // обьявление переменной
        int secondCatAge;  // обьявление переменной
    }
}
